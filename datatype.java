public class datatype {

      public static void main(String[] args) {
         byte a= 127;
         System.out.println(a);
         short b= 32767;
         System.out.println(b);
         int c=2147483647;
         System.out.println(c);
         long d= 100000;
         System.out.println(d);
         float e= 12.35f;
         System.out.println(e);
         double f= 12.3444555;
         System.out.println(f);
         char k= 'a';
         System.out.println(k);
      }
    
}
