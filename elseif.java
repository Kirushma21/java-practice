public class elseif {
    public static void main(String[] args) {
        int score=70;
        if(score>35 && score<60){
            System.out.println("video game");
        }
        else if(score>60 && score<90){
            System.out.println("iphone");
        }
        else if(score>90){
            System.out.println("mac book");
        }
    }
    
}
